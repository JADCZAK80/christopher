    <h3>1. Qu'est-ce qu'un Cookie?</h3>

    <p>Un cookie est un petit fichier texte qui est stocké sur votre ordinateur ou appareil mobile par le navigateur web lorsque vous visitez notre site. Les cookies permettent au site de se souvenir de vos actions et préférences (telles que la connexion, la langue, la taille de la police et d'autres préférences d'affichage) sur une période de temps, afin que vous n'ayez pas à les saisir à chaque visite ou lorsque vous naviguez d'une page à une autre.</p>

    <h3>2. Comment Utilisons-Nous les Cookies?</h3>

    <p>Nous utilisons des cookies à des fins diverses, notamment pour comprendre comment vous interagissez avec notre site, pour analyser et améliorer les performances du site, et pour vous fournir du contenu personnalisé.</p>

    <h3>3. Types de Cookies Utilisés</h3>

    <ul>
        <li><strong>Cookies Essentiels :</strong> Ces cookies sont nécessaires au fonctionnement du site et ne peuvent pas être désactivés dans nos systèmes.</li>
        <li><strong>Cookies de Performance :</strong> Ces cookies nous aident à comprendre comment les visiteurs interagissent avec le site en collectant et rapportant des informations de manière anonyme.</li>
        <li><strong>Cookies de Fonctionnalité :</strong> Ces cookies permettent au site de se souvenir de vos choix et préférences, fournissant ainsi une expérience plus personnalisée.</li>
    </ul>

    <h3>4. Comment Gérer les Cookies</h3>

    <p>Vous pouvez contrôler et/ou supprimer les cookies comme vous le souhaitez. Vous pouvez supprimer tous les cookies déjà présents sur votre ordinateur et vous pouvez configurer la plupart des navigateurs pour les empêcher d'être placés. Si vous le faites, cependant, vous devrez peut-être ajuster manuellement certaines préférences à chaque visite sur un site, et certaines fonctionnalités du site peuvent ne pas fonctionner comme prévu.</p>

